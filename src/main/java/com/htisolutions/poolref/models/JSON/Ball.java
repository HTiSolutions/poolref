package com.htisolutions.poolref.models.JSON;

public class Ball {

    private Integer ballNum;

    private Integer x;

    private Integer y;

    public Integer getBallNum() {
        return this.ballNum;
    }

    public void setBallNum(Integer ballNum) {
        this.ballNum = ballNum;
    }

    public Integer getX() {
        return this.x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return this.y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
